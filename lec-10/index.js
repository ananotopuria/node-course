// / 1)რა განსხვავებაა let, const შორის?

/* 

let და const ორივე block-scoped ცვლადებია. მთავარი განსხვავება ისაა, 
რომ let-ის მნიშვნელობის შეცვლა შესაძლებელია, ხოლო const-ის — არა. 
თუმცა თუ const-ში ობიექტი ან მასივია შენახული, მისი შიგთავსის შეცვლა შეგვიძლია, 
რადგან უცვლელი მხოლოდ reference-ია და არა თვითონ ობიექტის შიგთავსი.

*/

// // 2)რა განსხვავებაა primitive და nonprimitive type-ებს შორის?

/* 

Primitive: string, number, boolean, null, undefined, symbol, bigint — მნიშვნელობა ინახება პირდაპირ.
Non-primitive: object, array, function — ინახება რეფერენსით

*/

// // 3)რა განსხვავებაა == და === ოპერატორებს შორის?

/* 

== loose equality ადარებს მხოლოდ მნიშვლეობებს
=== strict equality ადარებს მნიშვნელობებს და ტიპებს

*/

// // 4)რა ტიპის არის null

/* 

null არის ცარიელი მნიშვნელობა
typeof null არის object

*/

// // 5)რა განსხვავებაა funtion და = () => {} შორის

/*

Function Declaration და Arrow Function განსხვავებული სინტაქსით იწერება. 
Function Declaration-ში ვიყენებთ function keyword-ს, ფუნქციის სახელს და შემდეგ ფუნქციის body-ს. 
Arrow Function-ში ვიყენებთ => სინტაქსს.

Function Declaration სრულად hoist-დება და მისი გამოძახება შექმნამდე შეგვიძლია, 
ხოლო Arrow Function (თუ let ან const-შია შენახული) შექმნამდე გამოძახება არ შეიძლება.

Arrow Function-ს საკუთარი this არ აქვს და იღებს გარე scope-დან (lexical this), 
ხოლო Function Declaration-ს საკუთარი this აქვს.

Arrow Function-ში arguments ობიექტი არ არსებობს, ხოლო ჩვეულებრივ ფუნქციაში არსებობს.

Arrow Function-ში, თუ body ერთი expression-ია, შეგვიძლია return არ დავწეროთ.
*/

// // 6)რას ნიშნავს callback function?

/*

Callback Function არის ფუნქცია, რომელიც სხვა ფუნქციას გადაეცემა არგუმენტად და მოგვიანებით გამოიძახება.
Callback-ებს ხშირად ვიყენებთ ასინქრონულ ოპერაციებში, ასევე მასივის მეთოდებში, როგორიცაა map, filter, reduce, forEach.

*/

// // 7)რა განსხვავებაა map, filter და reduce-ს შორის?

/*

map, filter და reduce მასივის მეთოდებია, მაგრამ სხვადასხვა დანიშნულება აქვთ.
map გადაუვლის ყველა ელემენტს და დააბრუნებს იმავე ზომის ახალ მასივს შეცვლილი მნიშვნელობებით.
filter გადაუვლის ყველა ელემენტს და დააბრუნებს მხოლოდ იმ ელემენტებს, რომლებიც პირობას აკმაყოფილებენ.
reduce მთელ მასივს "შეაჯამებს" ან გადააქცევს ერთ მნიშვნელობად.

*/

// // 8)რა არის Promise?

/*

Promise არის JavaScript-ის ობიექტი, რომელიც წარმოადგენს ასინქრონული ოპერაციის მომავალ შედეგს.
როდესაც ოპერაცია მაშინვე ვერ სრულდება (მაგალითად API request), Promise გვაძლევს საშუალებას დაველოდოთ შედეგს.

*/

// // 9)რა განსხვავებაა Promise.all, Promise.race და Promise.any-ს შორის?

/*

Promise.all, Promise.race და Promise.any რამდენიმე Promise-ის ერთად მართვის მეთოდებია, მაგრამ განსხვავებულად მუშაობენ.
Promise.all ელოდება ყველას, Promise.race აბრუნებს პირველს ვინც დასრულდება, ხოლო Promise.any აბრუნებს პირველს ვინც წარმატებით დასრულდება.

*/

// // 10)რას ნიშნავს async/await და რატომ ვიყენებთ?

/*

async/await არის Promise-ებთან მუშაობის უფრო მარტივი და წაკითხვადი სინტაქსი.
async ფუნქცია ყოველთვის აბრუნებს Promise-ს, ხოლო await აჩერებს ფუნქციის შესრულებას მანამ, სანამ Promise არ დასრულდება.

*/

// // 11)რა განსხვავებაა class-სა და object-ს შორის?

/*

Class არის შაბლონი (blueprint), რომლის მიხედვითაც იქმნება ობიექტები.
Object არის ამ კლასის კონკრეტული ეგზემპლარი (instance), რომელსაც აქვს საკუთარი მონაცემები და მეთოდები.

*/

// // 12)რას აკეთებს constructor class-ში?

/*

Constructor არის სპეციალური მეთოდი class-ში, რომელიც ავტომატურად გამოიძახება მაშინ, როცა new keyword-ით ახალ ობიექტს ვქმნით.
მისი მთავარი დანიშნულებაა ობიექტის საწყისი მნიშვნელობებით ინიციალიზაცია.
იქმნება ახალი ცარიელი ობიექტი.
this იწყებს ამ ახალ ობიექტზე მითითებას.
constructor სრულდება და ინიციალიზდება/ენიჭება საწყისი მონაცემები.
ბოლოს ავტომატურად ბრუნდება შექმნილი ობიექტი.

*/

